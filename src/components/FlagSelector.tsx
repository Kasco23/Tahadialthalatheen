const FlagSelector: React.FC<FlagSelectorProps> = ({
  selectedFlag,
  onFlagSelect,
  title = "Select Your Flag",
  className = "",
  "data-testid": testId,
}) => {
  const [searchTerm, setSearchTerm] = useState("");
  const [isOpen, setIsOpen] = useState(false);

  const filteredCountries = COUNTRIES.filter(
    (country) =>
      country.name.toLowerCase().includes(searchTerm.toLowerCase()) ||
      country.code.toLowerCase().includes(searchTerm.toLowerCase()),
  );

  const selectedCountry = COUNTRIES.find((c) => c.code === selectedFlag);

  return (
    <div
      className={`w-full max-w-md mx-auto ${className}`}
      data-testid={testId}
    >
      <h3 className="text-lg font-semibold text-gray-800 mb-3 text-center">
        {title}
      </h3>

      <div className="relative">
        {/* Selected Flag Display / Dropdown Trigger */}
        <button
          type="button"
          onClick={() => setIsOpen(!isOpen)}
          className="w-full p-3 bg-white border border-gray-300 rounded-lg shadow-sm hover:shadow-md transition-shadow duration-200 flex items-center justify-between"
          aria-expanded={isOpen}
          aria-haspopup="listbox"
        >
          <div className="flex items-center space-x-3">
            {selectedCountry ? (
              <>
                <Flag
                  countryCode={selectedCountry.code}
                  svg
                  style={{ width: "24px", height: "18px" }}
                />
                <span className="text-gray-700">{selectedCountry.name}</span>
              </>
            ) : (
              <span className="text-gray-400">Choose a flag...</span>
            )}
          </div>
          <svg
            className={`w-5 h-5 text-gray-400 transform transition-transform duration-200 ${
              isOpen ? "rotate-180" : ""
            }`}
            fill="none"
            stroke="currentColor"
            viewBox="0 0 24 24"
          >
            <path
              strokeLinecap="round"
              strokeLinejoin="round"
              strokeWidth={2}
              d="M19 9l-7 7-7-7"
            />
          </svg>
        </button>

        {/* Dropdown Panel */}
        {isOpen && (
          <div className="absolute z-50 w-full mt-2 bg-white border border-gray-300 rounded-lg shadow-lg max-h-80 overflow-hidden">
            {/* Search Input */}
            <div className="p-3 border-b border-gray-200">
              <input
                type="text"
                placeholder="Search countries..."
                value={searchTerm}
                onChange={(e) => setSearchTerm(e.target.value)}
                className="w-full p-2 border border-gray-300 rounded-md text-sm focus:ring-2 focus:ring-blue-500 focus:border-blue-500"
                autoFocus
              />
            </div>

            {/* Countries List */}
            <div
              className="overflow-y-auto max-h-60"
              role="listbox"
              aria-label="Country selection"
            >
              {filteredCountries.length === 0 ? (
                <div className="p-4 text-gray-500 text-center text-sm">
                  No countries found
                </div>
              ) : (
                filteredCountries.map((country) => (
                  <button
                    key={country.code}
                    type="button"
                    role="option"
                    aria-selected={selectedFlag === country.code}
                    onClick={() => {
                      onFlagSelect(country.code);
                      setIsOpen(false);
                      setSearchTerm("");
                    }}
                    className={`w-full px-4 py-3 flex items-center space-x-3 hover:bg-gray-50 transition-colors duration-150 ${
                      selectedFlag === country.code
                        ? "bg-blue-50 text-blue-700"
                        : "text-gray-700"
                    }`}
                  >
                    <Flag
                      countryCode={country.code}
                      svg
                      style={{ width: "24px", height: "18px" }}
                    />
                    <span className="text-left">{country.name}</span>
                    {selectedFlag === country.code && (
                      <svg
                        className="w-4 h-4 text-blue-600 ml-auto"
                        fill="currentColor"
                        viewBox="0 0 20 20"
                      >
                        <path
                          fillRule="evenodd"
                          d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                          clipRule="evenodd"
                        />
                      </svg>
                    )}
                  </button>
                ))
              )}
            </div>
          </div>
        )}

        {/* Click Outside Handler */}
        {isOpen && (
          <div
            className="fixed inset-0 z-0"
            onClick={() => {
              setIsOpen(false);
              setSearchTerm("");
            }}
            aria-hidden="true"
          />
        )}
      </div>
    </div>
  );
};

export default FlagSelector;
